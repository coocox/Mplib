/**
 * @brief  This function handles COMP interrupt request.
 * @param  None
 * @retval None
 */
void COMP_IRQHandler(void) {
	if (EXTI_GetITStatus(EXTI_Line21)) {
		/* Clear EXTI Line21 (COMP1) interrupt pending bit */
		EXTI_ClearITPendingBit(EXTI_Line21);

		/* Toggle LED1 */
		GPIO_ToggleBits(GPIOB, LD_GREEN_GPIO_PIN | LD_BLUE_GPIO_PIN);
	}
}

